		std::cout << "Введите сторону треугольника:\n"; cin >> a;
		cout<<"Площадь треугольника = "<<area(a)<<"\n";
		system("pause");
		return 0;
	case 2:
		std::cout << "Введите стороны треугольника:\n"; cin >> a; cin >> b; cin >> c;
