#include <iostream>
using namespace std;

double area(double a) 
{
	double half_p = a * 3 / 2;
	double s = sqrt(half_p * (half_p - a) * 3);
	return s;
}

double area(double a, double b, double c)
{
	double half_p = (a + b + c) / 2;
	double s = sqrt(half_p * (half_p - a) * (half_p-b)* (half_p - c));
	return s;
}
